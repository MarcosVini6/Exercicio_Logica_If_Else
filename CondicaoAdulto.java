package lutas.gloriacondicionaisifelse;

public class CondicaoAdulto {
	public static void main(String[] args) {
		int idade = 15;
		if (idade>18) {
			System.out.println("Você tem "+ idade + " então é maior de idade.");
			
		}
		else {
			System.out.println("Você tem "+ idade + " então é menor de idade.");
		}
	}

}
